/**
 * Emits SQL: INSERT for platform_qa_week_topic (explicit per week).
 * 108 weeks for lanes 1–10, 40 for lane 11. Each line is unique (lane + week_index).
 * Run: node scripts/generate-qa-explicit-topics-sql.mjs > /tmp/qa.sql
 */
const CATALOG = 'qa_spine_v1';

const STAGES = [
  'Hook & safety', 'Direct instruction', 'Guided practice', 'Independent build', 'Debug clinic',
  'Rubric & peer review', 'Sprint A', 'Sprint B', 'Integration', 'Showcase','Portfolio', 'Reteach & stretch',
];

const LANE_LABEL = {
  1: 'Blocks / Scratch (Young Innovator · Basic 1)',
  2: 'Blocks / Scratch (Young Innovator · Basic 2)',
  3: 'Blocks / Scratch (Young Innovator · Basic 3)',
  4: 'Python (Basic 4 path)',
  5: 'HTML & CSS (Basic 4 path)',
  6: 'Python (Basic 5 path)',
  7: 'HTML & CSS (Basic 5 path)',
  8: 'Python (Basic 6 path)',
  9: 'HTML & CSS (Basic 6 path)',
  10: 'JSS web app (JS/React/Tailwind)',
  11: 'JSS web app (Year 1 partial · JSS2 lane)',
};

const NIGERIA = [
  'market pricing', 'traffic flow', 'hospital triage', 'farm yield', 'mobile money', 'solar charging',
  'sports stats', 'music playlist', 'school timetable', 'local news digest', 'waste collection', 'bus arrival times',
];

const esc = (s) => String(s).replace(/'/g, "''");

function topicFor(lane, w) {
  const y = Math.floor((w - 1) / 36) + 1;
  const t = Math.floor(((w - 1) % 36) / 12) + 1;
  const wk = ((w - 1) % 12) + 1;
  const stage = STAGES[(w - 1) % STAGES.length];
  const nia = NIGERIA[(w + lane) % NIGERIA.length];
  return `${LANE_LABEL[lane]} | Y${y} T${t} W${wk} — ${stage} — tie-in: ${nia}`;
}

const rows = [];
for (let lane = 1; lane <= 11; lane++) {
  const maxW = lane === 11 ? 40 : 108;
  for (let w = 1; w <= maxW; w++) {
    const title = topicFor(lane, w);
    const sub = JSON.stringify(['Explicit QA seed', 'Nigeria link', 'Rubric ready']).replace(/'/g, "''");
    rows.push(
      `  ('${CATALOG}', ${lane}, ${w}, '${esc(title)}', '${sub}'::jsonb, jsonb_build_object('explicit_seed', true, 'lane', ${lane}, 'week_index', ${w}))`
    );
  }
}

const sql = `-- generated by scripts/generate-qa-explicit-topics-sql.mjs
INSERT INTO public.platform_qa_week_topic (catalog_version, lane_index, week_index, base_topic, subtopics, metadata)
VALUES
${rows.join(',\n')}
ON CONFLICT (catalog_version, lane_index, week_index) DO UPDATE SET
  base_topic = excluded.base_topic,
  subtopics = excluded.subtopics,
  metadata = excluded.metadata;
`;
process.stdout.write(sql);
